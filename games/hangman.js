// Special class to be used by class Modal
class FieldRow {
  constructor(cname) {
    this.cname = cname;
    this.title;
    this.fields = [];
  }

  build() {
    if (this.fields.length) {
      //
      const $fieldTitle = $(`<div class="${this.cname}">${this.title}</div>`)
        .css("padding", "5px 0")
        .css("font-weight", 700);

      const $fieldContainer = $(`<div class="field-container"></div>`)
        .css("display", "flex")
        .css("flex-direction", "row")
        .css("flex", 1)
        .css("justify-content", "space-between")
        .css("text-align", "center");
      //
      for (const { name, value } of this.fields) {
        $(`<div class="field"></div>`)
          .appendTo($fieldContainer)
          .css("display", "flex")
          .css("flex-direction", "column")
          .css("flex", 1)
          .css("justify-content", "space-between")
          .append($(`<div>${name}</div>`).css("font-size", "small"))
          .append($(`<div class="fs-5">${value}</div>`));
      }
      //
      $fieldTitle.appendTo($("#mb-wq"));
      $fieldContainer.appendTo($("#mb-wq"));
    } else {
      console.error("FieldRow#build: Could not build empty fields");
    }
    return this;
  }

  setTitle(title) {
    this.title = title;
    return this;
  }

  addField(name, value) {
    this.fields.push({ name: String(name), value: String(value) });
    return this;
  }
}

// Targets id mb-wq <modal body wordquest>, make modal-body have this id
class Modal {
  constructor() {
    this.description = null;
    this.fields = [];
    this.$modalBody = $("#mb-wq");
  }

  /**
   *
   */
  build() {
    // Build desc
    if (this.description) {
      $(`<div class="fs-5 gmb-description">${this.description}</div>`)
        .css("text-align", "center")
        .css("padding-bottom", "20px")
        .appendTo(this.$modalBody);
    }

    // Build Fields
    if (this.fields.length) {
      for (const field of this.fields) {
        field.build();
      }
    }
  }

  addField(value) {
    if (!(value instanceof FieldRow))
      throw new Error("Invalid value: Field must be an instance of FieldRow");

    this.fields.push(value);
    return this;
  }

  setDescription(value) {
    this.description = String(value);
    return this;
  }

  reset(htmlReset) {
    this.description = null;
    this.fields = [];

    if (htmlReset) {
      this.$modalBody.html("");
    }

    return this;
  }
}

function getWord(categories = []) {
  if (!categories.length) categories = Object.keys(dictionary);

  const category = _.sample(categories);
  const word = _.sample(dictionary[category]).toUpperCase();

  return [category.replace(/_/gi, " ").toUpperCase(), word];
}

